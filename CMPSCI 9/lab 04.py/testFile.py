import pytest
from Stack import Stack
from lab04 import maze_path_exists

def test_maze_path_exists():
    maze = [
        ['+', '+', '+', '+', 'G', '+'],
        ['+', ' ', '+', ' ', ' ', '+'],
        ['+', ' ', ' ', ' ', '+', '+'],
        ['+', ' ', '+', '+', ' ', '+'],
        ['+', ' ', ' ', ' ', ' ', '+'],
        ['+', '+', '+', '+', '+', '+']
    ]
    assert maze_path_exists(maze, 4, 4) == True
    assert maze == [
        ['+', '+', '+', '+', 'G', '+'],
        ['+', 8, '+', 11, 12, '+'],
        ['+', 7, 9, 10, '+', '+'],
        ['+', 6, '+', '+', 2, '+'],
        ['+', 5, 4, 3, 1, '+'],
        ['+', '+', '+', '+', '+', '+']
    ]
def test_maze_path_exists_open_space():
    maze = [
        ['+', '+', '+', '+', '+', '+'],
        ['+', '', '', '', 'G', '+'],
        ['+', '', '', '', '', '+'],
        ['+', '', '', '', '', '+'],
        ['+', '', '', '', '', '+'],
        ['+', '', '', '', '', '+'],
        ['+', '+', '+', '+', '+', '+']
    ]
    assert maze_path_exists(maze, 5, 4) == True
    assert maze == [
        ['+', '+', '+', '+', '+', '+'],
        ['+', '', '', '', 'G', '+'],
        ['+', '', '', '', 4, '+'],
        ['+', '', '', '', 3, '+'], 
        ['+', '', '', '', 2, '+'],
        ['+', '', '', '', 1, '+'],
        ['+', '+', '+', '+', '+', '+']
    ]

def test_maze_path_exists_no_solution():
    maze = [
    ['+','+','+','+','G','+'],
    ['+',' ','+',' ','','+'],
    ['+','+',' ',' ','+','+'],
    ['+',' ','+','+',' ','+'],
    ['+',' ',' ',' ',' ','+'],
    ['+','+','+','+','+','+'] 
]
    assert maze_path_exists(maze, 3, 4) == False
    assert maze == [
        ['+', '+', '+', '+', 'G', '+'],
        ['+', '', '+', '', '', '+'],
        ['+', '+', '', '', '+', '+'],
        ['+', 6, '+', '+', 1, '+'],
        ['+', 5, 4, 3, 2, '+'],
        ['+', '+', '+', '+', '+', '+'] ]

